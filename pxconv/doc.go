// Пакет pxconv предоставляет инструменты для работы с единицами измерения
// пользовательского интерфейса, такими как независимые от устройства пиксели
// (dp) и единицы для шрифтов (sp), а также их преобразования в пиксели (px)
// с учетом плотности экрана.
//
// # Типы Dp и Sp
//
// Dp представляет собой единицы измерения, которые независимы от плотности пикселей экрана,
// используемые для задания размеров элементов интерфейса, таких как кнопки и изображения.
// Эти единицы обеспечивают адаптивность интерфейса на устройствах с разной плотностью пикселей,
// так как преобразование в пиксели зависит от плотности экрана.
//
// Sp представляет единицы измерения для текста, которые также предназначены для создания
// адаптивных пользовательских интерфейсов, но их размер зависит от плотности пикселей экрана,
// чтобы текст оставался читаемым на устройствах с разной плотностью. Оба типа используют float32 для хранения значений.
//
// Пример:
//
//	var size Dp = 16    // Размер в dp
//	var textSize Sp = 14 // Размер текста в sp
//
// # Тип Metric
//
// Metric предоставляет методы для конвертации значений между dp, sp и пикселями (px).
// Он использует два параметра: плотность пикселей на dp (PxPerDp) и плотность пикселей на sp (PxPerSp).
// Эти параметры учитываются при преобразованиях, чтобы адаптировать интерфейсы под различные экраны.
//
// Пример использования:
//
//	m := Metric{PxPerDp: 2.0, PxPerSp: 1.5}
//	px := m.DpToPx(10)  // px = 20
//	sp := m.DpToSp(10)  // sp = 13.33
//
// # Методы:
//
//   - DpToPx: Конвертирует dp в пиксели (px), округляя до ближайшего целого числа.
//     Пример:
//     m := Metric{PxPerDp: 2.0}
//     px := m.DpToPx(15) // px = 30
//
//   - SpToPx: Конвертирует sp в пиксели (px), округляя до ближайшего целого числа.
//     Пример:
//     m := Metric{PxPerSp: 1.5}
//     px := m.SpToPx(10) // px = 15
//
//   - DpToSp: Конвертирует dp в sp, используя плотности для dp и sp.
//     Пример:
//     m := Metric{PxPerDp: 2.0, PxPerSp: 1.5}
//     sp := m.DpToSp(10) // sp = 13.33
//
//   - SpToDp: Конвертирует sp в dp, используя плотности для dp и sp.
//     Пример:
//     m := Metric{PxPerDp: 2.0, PxPerSp: 1.5}
//     dp := m.SpToDp(15) // dp = 10
//
//   - PxToDp: Конвертирует пиксели (px) в dp, используя плотность пикселей на dp.
//     Пример:
//     m := Metric{PxPerDp: 2.0}
//     dp := m.PxToDp(20) // dp = 10
//
//   - PxToSp: Конвертирует пиксели (px) в sp, используя плотность пикселей на sp.
//     Пример:
//     m := Metric{PxPerSp: 1.5}
//     sp := m.PxToSp(15) // sp = 10
//
// Все методы учитывают плотность экрана, заданную в полях PxPerDp и PxPerSp.
// Если значения плотности экрана не заданы (или равны 0 или отрицательны),
// используется значение по умолчанию 1.
//
// Пакет отлично подходит для адаптации интерфейсов под экраны с разной плотностью,
// включая мобильные устройства, настольные приложения и встроенные системы.
// Он особенно полезен для создания отзывчивых и масштабируемых пользовательских интерфейсов.
package pxconv
